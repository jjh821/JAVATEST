package java_exam;

public class TypeCasting2 {

	public static void main(String[] args) {
		double dnum = 10.1;
		// 소수점 올림.
		dnum = Math.ceil(dnum);
		System.out.println(dnum);
		
		dnum = 10.9;
		dnum = Math.floor(dnum);
		System.out.println(dnum);
		
		dnum = 15.18;
		dnum = Math.round(dnum);
		System.out.println(dnum);
		dnum = 10.56;
		dnum = Math.round(dnum);
		System.out.println(dnum);
		
		
		
	

	}

}
