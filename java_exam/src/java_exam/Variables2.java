package java_exam;

public class Variables2 {

	public static void main(String[] args) {
		float fnum = 10.9f;
		int num = (int) fnum;
		System.out.println(fnum); // 10.5
		System.out.println(num); // 10.
		
		double dnum = 11.15;
		num = (int) dnum;
		System.out.println(dnum); // 11.15
		System.out.println(num); // 11.15
		

	}

}
