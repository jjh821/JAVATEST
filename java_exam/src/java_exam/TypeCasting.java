package java_exam;

public class TypeCasting {

	public static void main(String[] args) {
		float fnum = 10.9f;
		int num = (int) fnum ;
		System.out.println(fnum);
		System.out.println(num);
		
		double dnum = 11.15;
		num = (int) dnum;
		System.out.println(dnum);
		System.out.println(num);

	}

}
