package exam;

public class AcountBcount {
//두 정수 A와 B가 주어졌을 때, A와 B를 비교하는 프로그램을 작성하시오.
	public static void main(String[] args) {
		int A = 90;
		int B = 55;
		
		if(A > B) {
			System.out.println(A + " > " + B);			
		}
		else if(A < B) {
			System.out.println(A + " < " + B);
		}
		else if(A == B) {
			System.out.println(A + " = " + B);
		}

	}

}
